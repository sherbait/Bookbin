                <div class="list-group">
                    <a href="#" class="list-group-item list-group-item-action">New</a>
                    <a href="#" class="list-group-item list-group-item-action">Like New</a>
                    <a href="#" class="list-group-item list-group-item-action">Very Good</a>
                    <a href="#" class="list-group-item list-group-item-action">Good</a>
                    <a href="#" class="list-group-item list-group-item-action">Acceptable</a>
                    <a href="#" class="list-group-item list-group-item-action">Unacceptable</a>


                    New: Just like it sounds. A brand-new, unused, unread copy in perfect condition. The dust cover and original protective wrapping, if any, are intact. All supplementary materials are included and all access codes for electronic material, if applicable, are valid and/or in working condition. Books with markings of any kind on the cover or pages, books marked as "Bargain" or "Remainder," or with any other labels attached, may not be listed as New condition.

                    Like New: Dust cover is intact, with no nicks or tears. Spine has no signs of creasing. Pages are clean and not marred by notes or folds of any kind. May contain remainder marks on outside edges, which should be noted in listing comments.

                    Very Good: Pages and dust cover are intact and not marred by notes or highlighting. The spine is undamaged.

                    Good: All pages and cover are intact (including the dust cover, if applicable). Spine may show signs of wear. Pages may include limited notes and highlighting. May include "From the library of" labels.

                    Acceptable: All pages and the cover are intact, but the dust cover may be missing. Pages may include limited notes and highlighting, but the text cannot be obscured or unreadable.

                    Unacceptable: Has missing pages and obscured or unreadable text. We also do not permit the sale of advance reading copies, including uncorrected proofs, of in-print or not-yet-published books.

